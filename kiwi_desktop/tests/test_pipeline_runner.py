def test_runner_completes_all_stages(tmp_path: Path) -> None:
    md = tmp_path / "a.md"
    # Content must exceed the small-file lane threshold (260 chars) and contain no
    # relevance keywords so the file lands on the relevance gate (fallback) path.
    _GENERIC = (
        "This document was generated by the automated test harness to confirm each stage "
        "of execution completes in sequence. The content here is intentionally neutral and "
        "free of any domain-specific signals. No business, technical, or career-related "
        "terms appear in this block of text at all."
    )
    md.write_text(_GENERIC, encoding="utf-8")
    d = Database(Path(":memory:"))
    d.connect()
    files = FileRepository(d)
    files.insert(path=str(md.resolve()), display_name="a.md")

    norm = tmp_path / "normalized"
    exports = tmp_path / "exports"
    out = PipelineRunner(d, normalized_work_dir=norm, export_root=exports).run()
    assert out.files_started == 1
    assert out.files_finished_ok == 1
    assert out.files_marked_failed == 0

    rec = files.get_by_path(str(md.resolve()))
    assert rec is not None
    assert rec.runner_status == RunnerStatus.COMPLETED.value
    assert rec.pipeline_next_stage is None
    assert rec.last_error is None
    assert rec.stage_checkpoint is not None
    payload = json.loads(rec.stage_checkpoint)
    assert payload["normalized_path"]
    assert isinstance(payload["chunks"], list)
    assert isinstance(payload["chunk_metadata"], list)
    assert payload["chunk_count"] == len(payload["chunks"])
    assert payload["export_profile"] == "anythingllm"
    assert payload.get("workspace") is None
    assert payload["category"] == "other"
    assert payload["matched_by"] == "fallback"
    assert payload["review_required"] is True
    assert payload["classification_reason"]
    assert Path(payload["export_source_path"]).is_file()
    assert (exports / "anythingllm" / "files_manifest.csv").is_file()
    assert (exports / "anythingllm" / "chunks_manifest.json").is_file()
    conn = d.connect()
    job_rows = conn.execute(
        "SELECT id, kind, status, result_json FROM jobs ORDER BY id DESC LIMIT 1"
    ).fetchall()
    assert len(job_rows) == 1
    jr = job_rows[0]
    assert jr["kind"] == "pipeline_run"
    assert jr["status"] == "completed"
    result_payload = json.loads(str(jr["result_json"]))
    assert result_payload["files_started"] == 1
    assert result_payload["files_finished_ok"] == 1


def test_runner_resumes_failed_file(tmp_path: Path) -> None:
    f = tmp_path / "b.txt"
    f.write_text("content", encoding="utf-8")
    d = Database(Path(":memory:"))
    d.connect()
    files = FileRepository(d)
    files.insert(path=str(f.resolve()), display_name="b.txt")

    def fail_once(record, stage: PipelineStage) -> None:
        if stage == PipelineStage.CLASSIFIED:
            raise RuntimeError("temporary")

    set_stage_handler(PipelineStage.CLASSIFIED, fail_once)
    try:
        out1 = PipelineRunner(
            d,
            normalized_work_dir=tmp_path / "n1",
            export_root=tmp_path / "e1",
        ).run()
        assert out1.files_marked_failed == 1
        rec = files.get_by_path(str(f.resolve()))
        assert rec is not None
        assert rec.runner_status == RunnerStatus.FAILED.value
        assert rec.pipeline_next_stage == PipelineStage.CLASSIFIED.value

        reset_pipeline_handlers()
        out2 = PipelineRunner(
            d,
            normalized_work_dir=tmp_path / "n2",
            export_root=tmp_path / "e2",
        ).run()
        assert out2.files_finished_ok == 1
        again = files.get_by_path(str(f.resolve()))
        assert again is not None
        assert again.runner_status == RunnerStatus.COMPLETED.value
    finally:
        reset_pipeline_handlers()


