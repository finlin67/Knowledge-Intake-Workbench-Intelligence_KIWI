def test_ollama_cache_by_content_hash(tmp_path: Path) -> None:
    f1 = tmp_path / "same1.md"
    f2 = tmp_path / "same2.md"
    f1.write_text("same content", encoding="utf-8")
    f2.write_text("same content", encoding="utf-8")
    d = Database(Path(":memory:"))
    d.connect()
    files = FileRepository(d)
    files.insert(path=str(f1.resolve()), display_name="same1.md", sha256="h1")
    files.insert(path=str(f2.resolve()), display_name="same2.md", sha256="h1")
    cfg = tmp_path / "classification_rules.json"
    cfg.write_text(
        json.dumps(
            {
                "enable_ollama": True,
                "ollama_model": "fake",
                "ai_mode": "ai_all",
                "FORCE_RULES": [],
                "COMPANY_MAP": {},
                "PROJECT_MAP": {},
                "CODE_EXT": {},
            }
        ),
        encoding="utf-8",
    )

    class CountingAI:
        def __init__(self):
            self.calls = 0

        def classify(self, *, file_path, file_name, preview_text):
            del file_path, file_name, preview_text
            self.calls += 1
            return AIClassificationResult(
                workspace="Archive",
                subfolder="cached",
                confidence=0.88,
                doc_type="reference",
                reasoning="first-call",
            )

    fake = CountingAI()
    runner = PipelineRunner(
        d,
        normalized_work_dir=tmp_path / "normalized",
        export_root=tmp_path / "exports",
        classification_config_path=cfg,
        ai_classifier=fake,
    )
    out = runner.run(max_files=2)
    assert out.files_finished_ok == 2
    assert fake.calls == 1
